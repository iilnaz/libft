#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*src;

	src = (char *)s;
	i = 0;
	if (!s)
		return ;
	while (i < n)
	{
		src[i] = 0;
		i++;
	}
}
